package pooex;

public class cliente {
			
			private String Nome;
			private double  Age; 
			private String End;
			
			public cliente ( String n, double i, String end) {
			
				Nome = n ;
				Age = i;
				End = end;
			
			}

			public String getNome() {
				return Nome;
			}

			public void setNome(String nome) {
				Nome = nome;
			}

			public double getAge() {
				return Age;
			}

			public void setAge(double age) {
				Age = age;
			}

			public String getEnd() {
				return End;
			}

			public void setEnd(String end) {
				End = end;
			}
			
			
		
									
					
}
